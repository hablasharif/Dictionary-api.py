# Initialize counts
total_words = len(words)
english_words = []
non_english_words = []

# Define a function to process each word
def process_word(word):
    definition = fetch_definition(word)
    if definition:
        english_words.append(word)
    else:
        non_english_words.append(word)

# Use a ThreadPoolExecutor for concurrent processing
with concurrent.futures.ThreadPoolExecutor() as executor:
    # Submit tasks for each word to the ThreadPoolExecutor
    futures = [executor.submit(process_word, word) for word in words]

    # Use tqdm for progress tracking
    with tqdm(total=total_words, desc="Processing words") as pbar:
        for future in concurrent.futures.as_completed(futures):
            pbar.update(1)  # Update progress bar

# Print counts
print(f"\nTotal input words: {total_words}")
print(f"Words that are English: {len(english_words)}")
print(f"Words that are not English: {len(non_english_words)}")

# Save results to files
with open("words_not_english.txt", "w", encoding="utf-8") as file:
    file.write("# Words that are not English\n\n")
    for word in non_english_words:
        file.write(f"- {word}\n")

with open("is_an_english_word.txt", "w", encoding="utf-8") as file:
    file.write("# Words that are English\n\n")
    for word in english_words:
        definition = fetch_definition(word)
        if definition:
            file.write(f"- {word}: {definition}\n")
